package com.capstone.plantplant.model;

public class UserDevice {
    private String kind;
    private String devicenum;
    private String humity;
    private String tempsenser;
    private String humsenser;
    private String deepsenser;
    private String date;

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getDevicenum() {
        return devicenum;
    }

    public void setDevicenum(String devicenum) {
        this.devicenum = devicenum;
    }

    public String getHumity() {
        return humity;
    }

    public void setHumity(String humity) {
        this.humity = humity;
    }

    public String getTempsenser() {
        return tempsenser;
    }

    public void setTempsenser(String tempsenser) {
        this.tempsenser = tempsenser;
    }

    public String getHumsenser() {
        return humsenser;
    }

    public void setHumsenser(String humsenser) {
        this.humsenser = humsenser;
    }

    public String getDeepsenser() {
        return deepsenser;
    }

    public void setDeepsenser(String deepsenser) {
        this.deepsenser = deepsenser;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
